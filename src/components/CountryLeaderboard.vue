import { getCountryFlag } from '@/utils/countryFlags'

interface CountryData {
  country: string
  totalViews: number
  athleteCount: number
}

const countries = ref<CountryData[]>([])
const isLoading = ref(true)

onMounted(async () => {
  isLoading.value = true
  try {
    const response = await fetch('/data/country_stats_2025.json')
    console.log('Country leaderboard fetch response:', response.status, response.ok)
    if (response.ok) {
      const data = await response.json()
      console.log('Country data loaded:', data)
      countries.value = data
    } else {
      console.error('Failed to fetch country data:', response.status)
    }
  } catch (error) {
    console.error('Error loading country leaderboard:', error)
  } finally {
    isLoading.value = false
  }
})

